<h1>Welcome to PsyPad!</h1>

<p>Psypad is an open source platform for facilitating visual psychophysics
experiments on an iPad.  

<p>The accompanying 
iPad app 
(available in the <a href="http://itunes.com/apps/psypad"> AppStore </a>) 
can run staircase or MOCS
procedures using a library of images that you create to 
determine visual stimuli.
Your image library can be built using a tool of your choice 
(eg R, MATLAB, etc), and uploaded through this server.
This server can also be used to configure tests, and 
process logfiles from tests that have been conducted on the iPad.



<p>To request an account on this server, email
<a href="mailto:aturpin@unimelb.edu.au">Andrew Turpin</a>.

<p>If you already have an account, login and navigate the site 
using the links above.</p>


